class setupTest {

	@Keyword
	def setupTestData(){
		WebUI.openBrowser('https://www.airbnb.com/')

		WebUI.maximizeWindow()

		//WebDriver driver = DriverFactory.getWebDriver()
		WebUI.navigateToUrl('https://www.airbnb.com/')

		WebUI.setText(findTestObject('HomePage_OR/input_Location'), GlobalVariable.location)

		WebUI.click(findTestObject('HomePage_OR/div_Add dates'))

		GlobalVariable.checkInExactDate = WebUI.getText(findTestObject('HomePage_OR/checkInDate'))

		WebUI.click(findTestObject('HomePage_OR/checkInDate'))

		GlobalVariable.checkOutExactDate = WebUI.getText(findTestObject('HomePage_OR/checkOutDate'))

		WebUI.click(findTestObject('HomePage_OR/checkOutDate'))

		WebUI.click(findTestObject('HomePage_OR/div_Add guests'))

		for (i in 1..GlobalVariable.adultsNum) {
			WebUI.click(findTestObject('HomePage_OR/btnAdultsIncrease'))
		}

		for (i in 1..GlobalVariable.childrenNum) {
			WebUI.click(findTestObject('HomePage_OR/btnChildrenIncrease'))
		}

		WebUI.click(findTestObject('HomePage_OR/btnSearch'))

		WebUI.click(findTestObject('ResultsPage_OR/btnCloseUnwantedSpansOnMap'))
	}

	/**
	 * Refresh browser
	 */
	@Keyword
	def refreshBrowser() {
		KeywordUtil.logInfo("Refreshing")
		WebDriver webDriver = DriverFactory.getWebDriver()
		webDriver.navigate().refresh()
		KeywordUtil.markPassed("Refresh successfully")
	}

	/**
	 * Click element
	 * @param to Katalon test object
	 */
	@Keyword
	def clickElement(TestObject to) {
		try {
			WebElement element = WebUiBuiltInKeywords.findWebElement(to);
			KeywordUtil.logInfo("Clicking element")
			element.click()
			KeywordUtil.markPassed("Element has been clicked")
		} catch (WebElementNotFoundException e) {
			KeywordUtil.markFailed("Element not found")
		} catch (Exception e) {
			KeywordUtil.markFailed("Fail to click on element")
		}
	}

	/**
	 * Get all rows of HTML table
	 * @param table Katalon test object represent for HTML table
	 * @param outerTagName outer tag name of TR tag, usually is TBODY
	 * @return All rows inside HTML table
	 */
	@Keyword
	def List<WebElement> getHtmlTableRows(TestObject table, String outerTagName) {
		WebElement mailList = WebUiBuiltInKeywords.findWebElement(table)
		List<WebElement> selectedRows = mailList.findElements(By.xpath("./" + outerTagName + "/tr"))
		return selectedRows
	}
}